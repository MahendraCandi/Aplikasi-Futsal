    @Override
    public void bersih() {
        txtNama.setText("");
        txtKapten.setText("");
        txtTelepon.setText("");
        cmbStatus.setSelectedIndex(0);
        txtCari.setText("");
        txtKode.setText(tCont.kodeTeamOto());
        showTableTeam();
        renderTable();
    }

    @Override
    public void simpan() {
        if("".equals(txtKode.getText())||"".equals(txtNama.getText())||"".equals(txtKapten.getText())||
                "".equals(txtTelepon.getText())){
            JOptionPane.showMessageDialog(null, "Data belum lengkap!");
        }else{
            Team team=tCont.findTeam(txtKode.getText());
            Team tm=new Team();
            if(team==null){
                tm.setKdTeam(txtKode.getText());
                tm.setTglDaftar(new Date());
                tm.setNmTeam(txtNama.getText());
                tm.setKapten(txtKapten.getText());
                tm.setNoHp(txtTelepon.getText());
                tm.setStatus(cmbStatus.getSelectedItem().toString());
                tm.setKeterangan("Belum Registrasi");
                try{
                    tCont.save(tm);
                }catch(Exception ex){}
                JOptionPane.showMessageDialog(null, "Data telah disimpan!");
            }else{
                tm.setKdTeam(txtKode.getText());
                try{
                SimpleDateFormat sdf=new SimpleDateFormat("dd MMMM yyyy", Locale.forLanguageTag("in-ID"));
                Date tgl=(Date) sdf.parse(txtTanggal.getText());
                tm.setTglDaftar(tgl);
            }catch(Exception ex){}
                tm.setNmTeam(txtNama.getText());
                tm.setKapten(txtKapten.getText());
                tm.setNoHp(txtTelepon.getText());
                tm.setStatus(cmbStatus.getSelectedItem().toString());
                tm.setKeterangan("Belum Registrasi");
                try{
                    tCont.update(tm);
                }catch(Exception ex){}
                JOptionPane.showMessageDialog(null, "Data telah diupdate!");
            }
            bersih();
        }
    }

    @Override
    public void hapus() {
        try{
            tCont.delete(txtKode.getText());
            JOptionPane.showMessageDialog(null, "Data telah di hapus!");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Data tidak bisa di hapus karena "+ex.getMessage());
        }
        bersih();
    }

