    private void seleksiTable(){
        tableTeam.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int baris=tableTeam.getSelectedRow();       //dapatkan baris yang terseleksi
                if(baris != -1){                        //jika baris bukan sama dengan -1 atau tidak ada
                    txtKode.setText(tableTeam.getValueAt(baris, 0).toString());
                    txtNama.setText(tableTeam.getValueAt(baris, 1).toString());
                    txtKapten.setText(tableTeam.getValueAt(baris, 2).toString());
                    txtTelepon.setText(tableTeam.getValueAt(baris, 3).toString());
                    SimpleDateFormat sdf=new SimpleDateFormat("dd MMMM yyyy", Locale.forLanguageTag("in-ID"));
                    txtTanggal.setText(sdf.format(tableTeam.getValueAt(baris, 4)));
                    cmbStatus.setSelectedItem(tableTeam.getValueAt(baris, 5).toString());
                }
            }
        });
    }
    
    public void renderTable(){
        TableCellRenderer tbr = new DefaultTableCellRenderer(){
            SimpleDateFormat sdf=new SimpleDateFormat("dd MMMM yyyy", Locale.forLanguageTag("in-ID"));
            public Component getTableCellRendererComponent(JTable table,
                    Object value, boolean isSelected, boolean hasFocus,
                    int row, int column){
                if(value instanceof Date){
                    value = sdf.format(value);
                }
                return super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            }
        };
        tableTeam.getColumnModel().getColumn(4).setCellRenderer(tbr);
    }
    
    private void cariTable(String cari){
        DefaultTableModel x=tCont.cari(model, cari);
        if(x.getRowCount()==0){
            JOptionPane.showMessageDialog(null, "Data tidak ada!");
        }else{
            tableTeam.setModel(tCont.cari(model, cari));
        }
    }

